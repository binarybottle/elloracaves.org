<h3>Search help:</h3>
To search for images, type in keywords and/or select a cave, then click on the "Search" button.
 <br />
 <br />
 To use multiple keywords, please select "Boolean" instead of "Normal" to perform a Boolean search.
 <br />
 &nbsp;&nbsp;&nbsp;&nbsp;
 Example: 
 &nbsp;&nbsp;&nbsp;&nbsp;
 +buddha -standing 
 <br />
 &nbsp;&nbsp;&nbsp;&nbsp;
(show all images described with the word "buddha" but not with "standing")
 <br />
 &nbsp;&nbsp;&nbsp;&nbsp;
 For further instructions, please refer to the relevant MySQL 
<a href="http://dev.mysql.com/doc/refman/5.0/en/fulltext-boolean.html" onClick="return popup(this,'MySQL help')">help</a> 
 page.



